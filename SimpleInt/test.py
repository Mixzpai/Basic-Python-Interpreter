# Import necessary components
from calc import *
#======================================================================================#
# REPL header
#======================================================================================#
def print_header():
    print("=" * 70)
    print(" Basic Interpreter REPL")
    print("=" * 70)
    print(" Type an expression and press Enter")
    print(" Ctrl+D (Linux/macOS) or Ctrl+Z + Enter (Windows) to exit")
    print("=" * 70)
#======================================================================================#
# Main REPL loop
#======================================================================================#
def main():
    while True:
        try:
            text = input('>>> ')
        except EOFError:
            break
        if not text:
            continue
        interpreter = Interpreter(text)
        result = interpreter.expr()
        print(result)

if __name__ == '__main__':
    print_header()
    main()
